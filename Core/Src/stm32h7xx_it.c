void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
  switch(GPIO_Pin){
  	case KB_IRQ_Pin: {
  		osEventFlagsSet(keypadEventHandle, EVENT_KEYBOARD_INT);
  		break;
  	}
  	case BATT_STATUS_INT_Pin: {
  		break;
  	case LCD_TE_Pin: {
  			signal_vsync();
  		break;
  	}
  	}
  }
  UNUSED(GPIO_Pin);

}
